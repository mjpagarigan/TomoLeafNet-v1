"""
Learning Curve Plotter for TOMOLeafNet
Loads saved training history from JSON and generates a detailed
learning curve with overfitting/underfitting diagnostics.

Usage:
    python RESULTS/plot_learning_curve.py

Requires: RESULTS/training_history.json (auto-generated by train.py)
"""
import os
import json
import numpy as np
import matplotlib
matplotlib.use('Agg')
import matplotlib.pyplot as plt

# --- PATHS ---
RESULTS_DIR = os.path.dirname(os.path.abspath(__file__))
JSON_PATH = os.path.join(RESULTS_DIR, 'training_history.json')
OUTPUT_PATH = os.path.join(RESULTS_DIR, 'LearningCurve.png')

# --- LOAD HISTORY ---
if not os.path.exists(JSON_PATH):
    print(f"[ERROR] History file not found: {JSON_PATH}")
    print(f"   Run 'python SCRIPTS/train.py' first to generate training history.")
    exit(1)

with open(JSON_PATH, 'r') as f:
    data = json.load(f)

loss = data['loss']
val_loss = data['val_loss']
acc = data['accuracy']
val_acc = data['val_accuracy']
auc = data.get('auc', [])      # Default to empty if old json
val_auc = data.get('val_auc', [])

phase1_end = data.get('phase1_epochs', 0)
total_epochs = len(loss)
epochs = range(1, total_epochs + 1)

print(f"[INFO] Loaded training history: {total_epochs} epochs (Phase 1: {phase1_end}, Phase 2: {total_epochs - phase1_end})")

# --- DIAGNOSTICS ---
def diagnose(train_vals, val_vals, metric_name):
    """Analyze final epochs for overfitting/underfitting."""
    # Look at last 5 epochs (or all if fewer)
    window = min(5, len(train_vals))
    train_end = np.mean(train_vals[-window:])
    val_end = np.mean(val_vals[-window:])
    gap = abs(train_end - val_end)
    
    if metric_name == 'accuracy':
        if train_end < 0.70:
            return 'UNDERFITTING', 'red'
        elif gap > 0.15:
            return 'OVERFITTING', 'orange'
        else:
            return 'GOOD FIT', 'green'
    else:  # loss
        if val_end > 1.0:
            return 'UNDERFITTING', 'red'
        elif gap > 0.3:
            return 'OVERFITTING', 'orange'
        else:
            return 'GOOD FIT', 'green'

acc_diag, acc_color = diagnose(acc, val_acc, 'accuracy')
loss_diag, loss_color = diagnose(loss, val_loss, 'loss')
auc_diag, auc_color = diagnose(auc, val_auc, 'accuracy') if auc else ('N/A', 'gray')

# --- PLOT ---
has_auc = len(auc) > 0
n_cols = 3 if has_auc else 2
fig, axes = plt.subplots(1, n_cols, figsize=(18 if has_auc else 12, 5), sharex=True)

# --- Loss Curve ---
ax1 = axes[0]
ax1.plot(epochs, loss, 'b-o', markersize=3, label='Train Loss', linewidth=1.5)
ax1.plot(epochs, val_loss, 'r-o', markersize=3, label='Val Loss', linewidth=1.5)

if phase1_end > 0:
    ax1.axvline(x=phase1_end, color='gray', linestyle='--', alpha=0.7, label=f'Fine-tune Start (epoch {phase1_end})')

# Shade gap region between train/val
ax1.fill_between(epochs, loss, val_loss, alpha=0.1, color='purple', label='Train-Val Gap')

# Best val loss marker
best_val_epoch = np.argmin(val_loss) + 1
best_val = min(val_loss)
ax1.annotate(f'Best: {best_val:.3f}', xy=(best_val_epoch, best_val),
             xytext=(best_val_epoch + 1, best_val + 0.1),
             arrowprops=dict(arrowstyle='->', color='darkred'),
             fontsize=9, color='darkred', fontweight='bold')

ax1.set_ylabel('Loss', fontsize=12)
ax1.set_title('Training & Validation Loss', fontsize=13, fontweight='bold')
ax1.legend(loc='upper right', fontsize=9)
ax1.grid(True, alpha=0.3)

# Diagnostic badge
ax1.text(0.02, 0.95, f'Diagnosis: {loss_diag}', transform=ax1.transAxes,
         fontsize=10, fontweight='bold', color='white',
         bbox=dict(boxstyle='round,pad=0.4', facecolor=loss_color, alpha=0.8),
         verticalalignment='top')

# --- Accuracy Curve ---
ax2 = axes[1]
ax2.plot(epochs, acc, 'b-o', markersize=3, label='Train Accuracy', linewidth=1.5)
ax2.plot(epochs, val_acc, 'r-o', markersize=3, label='Val Accuracy', linewidth=1.5)

if phase1_end > 0:
    ax2.axvline(x=phase1_end, color='gray', linestyle='--', alpha=0.7, label=f'Fine-tune Start (epoch {phase1_end})')

# Shade gap region
ax2.fill_between(epochs, acc, val_acc, alpha=0.1, color='purple', label='Train-Val Gap')

# Best val accuracy marker
best_acc_epoch = np.argmax(val_acc) + 1
best_acc = max(val_acc)
ax2.annotate(f'Best: {best_acc:.3f}', xy=(best_acc_epoch, best_acc),
             xytext=(best_acc_epoch + 1, best_acc - 0.05),
             arrowprops=dict(arrowstyle='->', color='darkgreen'),
             fontsize=9, color='darkgreen', fontweight='bold')

ax2.set_xlabel('Epoch', fontsize=12)
ax2.set_ylabel('Accuracy', fontsize=12)
ax2.set_title('Training & Validation Accuracy', fontsize=13, fontweight='bold')
ax2.legend(loc='lower right', fontsize=9)
ax2.grid(True, alpha=0.3)
ax2.set_xticks(range(1, total_epochs + 1))

# Diagnostic badge
ax2.text(0.02, 0.05, f'Diagnosis: {acc_diag}', transform=ax2.transAxes,
         fontsize=10, fontweight='bold', color='white',
         bbox=dict(boxstyle='round,pad=0.4', facecolor=acc_color, alpha=0.8),
         verticalalignment='bottom')

# --- AUC Curve ---
if has_auc:
    ax3 = axes[2]
    ax3.plot(epochs, auc, 'b-o', markersize=3, label='Train AUC', linewidth=1.5)
    ax3.plot(epochs, val_auc, 'r-o', markersize=3, label='Val AUC', linewidth=1.5)

    if phase1_end > 0:
        ax3.axvline(x=phase1_end, color='gray', linestyle='--', alpha=0.7, label=f'Fine-tune Start')

    # Shade gap region
    ax3.fill_between(epochs, auc, val_auc, alpha=0.1, color='purple', label='Train-Val Gap')

    # Best val auc marker
    best_auc_epoch = np.argmax(val_auc) + 1
    best_auc = max(val_auc)
    ax3.annotate(f'Best: {best_auc:.3f}', xy=(best_auc_epoch, best_auc),
                 xytext=(best_auc_epoch + 1, best_auc - 0.05),
                 arrowprops=dict(arrowstyle='->', color='darkgreen'),
                 fontsize=9, color='darkgreen', fontweight='bold')

    ax3.set_xlabel('Epoch', fontsize=12)
    ax3.set_ylabel('AUC', fontsize=12)
    ax3.set_title('Training & Validation AUC', fontsize=13, fontweight='bold')
    ax3.legend(loc='lower right', fontsize=9)
    ax3.grid(True, alpha=0.3)
    ax3.set_xticks(range(1, total_epochs + 1))

# --- Final Summary ---
fig.suptitle('TomoLeafNet v3 — Learning Curve Analysis', fontsize=15, fontweight='bold', y=0.98)

# Summary text at bottom
final_train_acc = acc[-1] * 100
final_val_acc = val_acc[-1] * 100
summary = (f"Final Train Acc: {final_train_acc:.1f}%  |  "
           f"Final Val Acc: {final_val_acc:.1f}%  |  "
           f"Best Val Acc: {best_acc*100:.1f}% (epoch {best_acc_epoch})  |  "
           f"Total Epochs: {total_epochs}")
fig.text(0.5, 0.01, summary, ha='center', fontsize=10, style='italic',
         bbox=dict(boxstyle='round', facecolor='lightyellow', alpha=0.8))

plt.tight_layout(rect=[0, 0.03, 1, 0.96])
plt.savefig(OUTPUT_PATH, dpi=150, bbox_inches='tight')
plt.close()

print(f"\n[DONE] Learning curve saved to: {OUTPUT_PATH}")
print(f"   Loss diagnosis:     {loss_diag}")
print(f"   Accuracy diagnosis: {acc_diag}")
if has_auc:
    print(f"   AUC diagnosis:      {auc_diag}")
print(f"   Final train acc:    {final_train_acc:.1f}%")
print(f"   Final val acc:      {final_val_acc:.1f}%")
print(f"   Best val acc:       {best_acc*100:.1f}% (epoch {best_acc_epoch})")
